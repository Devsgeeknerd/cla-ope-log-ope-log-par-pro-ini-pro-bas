// Operador lógico E ("&&").
console.log(true && true);
console.log(true && false);
console.log(false && true);
console.log(false && false);

// true se x estiver entre 18 e 90, caso contrário, false.
let x: number = 50;
console.log(x >= 18 && x <= 90);

// Operador lógico OU ("||").
console.log(true || true);
console.log(true || false);
console.log(false || true);
console.log(false || false);

// true se y for menor que 10 ou maior que 20, caso contrário, false.
let y: number = 50;
console.log(y < 10 || y > 20);

// Operador lógico NÃO ("!").
console.log(!true);
console.log(!false);
